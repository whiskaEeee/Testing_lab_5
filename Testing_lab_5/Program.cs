namespace Testing_lab_5
{

    internal class Program
    {
        static void Main()
        {
            

            IWebDriver driver = new ChromeDriver();
            
            try
            {

                driver.Navigate().GoToUrl("https://demoqa.com");

                TestRegistration(driver);
                TestBookSearch(driver);
                TestLoginButton(driver);
            }
            finally
            {
                driver.Quit();
            }
        }

        static void TestRegistration(IWebDriver driver)
        {
            driver.Navigate().GoToUrl("https://demoqa.com/register");
            Thread.Sleep(1000);

            driver.FindElement(By.Id("firstname")).SendKeys("Donald");
            driver.FindElement(By.Id("lastname")).SendKeys("Trump");
            driver.FindElement(By.Id("userName")).SendKeys("testuser@example.com");
            driver.FindElement(By.Id("password")).SendKeys("password123");

            //закоментировал так как там проверка короче на reCapcha и он нажать кнопку не может 
            //если добавить Thread.Sleep("time"); можно успеть самому нажать но тут и так видно что рабоатет 
            //driver.FindElement(By.Id("register")).Click();

            Thread.Sleep(1000);

            Console.WriteLine("Регистрация протестирована.");
            Console.ReadKey();
        }

        static void TestLoginButton(IWebDriver driver)
        {
            driver.Navigate().GoToUrl("https://demoqa.com/login");
            Thread.Sleep(1000);

            driver.FindElement(By.Id("userName")).SendKeys("Putin");
            driver.FindElement(By.Id("password")).SendKeys("ZvZvZv228@");
            var loginButton = driver.FindElement(By.Id("login"));
            loginButton.Click();

            Thread.Sleep(1000);

            Console.WriteLine("Проверена работа кнопки 'Login'.");
            Console.ReadKey();
        }

        static void TestBookSearch(IWebDriver driver)
        {
            driver.Navigate().GoToUrl("https://demoqa.com/books");
            Thread.Sleep(1000);

            var searchBox = driver.FindElement(By.Id("searchBox"));
            searchBox.SendKeys("Git Pocket Guide");
            Thread.Sleep(500);

            var bookLink = driver.FindElement(By.XPath("//a[text()='Git Pocket Guide']"));


            bool isBookDisplayed = bookLink.Displayed;

            Console.WriteLine($"Проверка поля поиска в каталоге книг: книга найдена - {isBookDisplayed}");
            Console.ReadKey();
        }


    }
